async def send_message_with_image(user_id: int, image_path: str, text: str):
    """Отправляет сообщение с картинкой."""
    try:
        if os.path.exists(image_path):
            photo = FSInputFile(image_path)
            await bot.send_photo(
                chat_id=user_id,
                photo=photo,
                caption=text
            )
        else:
            await bot.send_message(user_id, text)
            logger.warning(f"Картинка {image_path} не найдена, отправлен только текст")
    except Exception as e:
        logger.error(f"Ошибка отправки сообщения пользователю {user_id}: {e}")
        try:
            await bot.send_message(user_id, text)
        except:
            pass


async def send_warmup_messages():
    """Отправляет сообщения прогрева с картинками."""
    for i, message_data in enumerate(WARMUP_MESSAGES):
        await asyncio.sleep(i * 86400)
        users = await get_users_with_free_guide_not_bought()
        for user in users:
            user_id = user[0]
            await send_message_with_image(
                user_id,
                message_data["image"],
                message_data["text"]
            )
            logger.info(f"Прогрев день {i + 1} отправлен пользователю {user_id}")

    await asyncio.sleep(2 * 86400)
    users = await get_users_with_free_guide_not_bought()
    for user in users:
        user_id = user[0]
        await send_message_with_image(
            user_id,
            WARMUP_FOLLOWUP["image"],
            WARMUP_FOLLOWUP["text"]
        )
        logger.info(f"Фоллоу-ап отправлен пользователю {user_id}")


async def send_feedback_requests():
    """Отправляет запросы обратной связи после покупки."""
    await asyncio.sleep(86400)
    users_499 = await get_users_who_bought_499()
    for user in users_499:
        user_id = user[0]
        try:
            await bot.send_message(user_id, FEEDBACK_499)
            logger.info(f"Обратная связь (499) отправлена пользователю {user_id}")
        except Exception as e:
            logger.error(f"Ошибка отправки обратной связи пользователю {user_id}: {e}")

    users_999 = await get_users_who_bought_999()
    for user in users_999:
        user_id = user[0]
        try:
            await bot.send_message(user_id, FEEDBACK_999)
            logger.info(f"Обратная связь (999) отправлена пользователю {user_id}")
        except Exception as e:
            logger.error(f"Ошибка отправки обратной связи пользователю {user_id}: {e}")


async def start_scheduler():
    """Запускает все задачи авторассылки."""
    asyncio.create_task(send_warmup_messages())
    asyncio.create_task(send_feedback_requests())
    logger.info("Планировщик авторассылки запущен")